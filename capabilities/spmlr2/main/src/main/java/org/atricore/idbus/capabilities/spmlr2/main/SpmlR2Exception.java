package org.atricore.idbus.capabilities.spmlr2.main;

/**
 * @author <a href=mailto:sgonzalez@atricor.org>Sebastian Gonzalez Oyuela</a>
 */
public class SpmlR2Exception extends Exception {

    public SpmlR2Exception() {
        super();
    }

    public SpmlR2Exception(String message) {
        super(message);
    }

    public SpmlR2Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public SpmlR2Exception(Throwable cause) {
        super(cause);
    }
}


