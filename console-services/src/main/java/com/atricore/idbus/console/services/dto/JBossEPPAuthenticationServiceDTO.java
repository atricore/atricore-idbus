public class JBossEPPAuthenticationServiceDTO extends AuthenticationServiceDTO {

    private static final long serialVersionUID = 6035914548009590586L;

    private String host;
    private String port;
    private String context;

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getContext() {
        return context;
    }

    public void setContext(String context) {
        this.context = context;
    }


}
