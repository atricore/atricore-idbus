package com.atricore.liveservices.liveupdate._1_0.util;

public class LiveUpdateSignatureException extends Exception {

    public LiveUpdateSignatureException() {
        super();
    }

    public LiveUpdateSignatureException(String message) {
        super(message);
    }

    public LiveUpdateSignatureException(String message, Throwable cause) {
        super(message, cause);
    }

    public LiveUpdateSignatureException(Throwable cause) {
        super(cause);
    }
}