package com.atricore.liveservices.liveupdate._1_0.util;

public class LiveUpdateKeyResolverException extends Exception {

    public LiveUpdateKeyResolverException() {
        super();
    }

    public LiveUpdateKeyResolverException(String message) {
        super(message);
    }

    public LiveUpdateKeyResolverException(String message, Throwable cause) {
        super(message, cause);
    }

    public LiveUpdateKeyResolverException(Throwable cause) {
        super(cause);
    }
}
