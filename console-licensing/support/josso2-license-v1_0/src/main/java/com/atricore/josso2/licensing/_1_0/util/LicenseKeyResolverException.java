package com.atricore.josso2.licensing._1_0.util;

public class LicenseKeyResolverException extends Exception {

    public LicenseKeyResolverException() {
        super();
    }

    public LicenseKeyResolverException(String message) {
        super(message);
    }

    public LicenseKeyResolverException(String message, Throwable cause) {
        super(message, cause);
    }

    public LicenseKeyResolverException(Throwable cause) {
        super(cause);
    }
}