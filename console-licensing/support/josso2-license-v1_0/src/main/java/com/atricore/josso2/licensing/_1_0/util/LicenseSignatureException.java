package com.atricore.josso2.licensing._1_0.util;

/**
 * Author: Dejan Maric
 */
public class LicenseSignatureException extends Exception {

    public LicenseSignatureException() {
        super();
    }

    public LicenseSignatureException(String message) {
        super(message);
    }

    public LicenseSignatureException(String message, Throwable cause) {
        super(message, cause);
    }

    public LicenseSignatureException(Throwable cause) {
        super(cause);
    }
}
